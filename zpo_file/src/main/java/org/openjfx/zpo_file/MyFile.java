package org.openjfx.zpo_file;

/**
 *
 * @author Adam
 */
public class MyFile {

    private String name;
    private String ext;
    private String size;
    private String date;

    public MyFile(String name, String ext, String size, String date) {
        this.name = name;
        this.ext = ext;
        this.size = size;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getExt() {
        return ext;
    }

    public void setExt(String ext) {
        this.ext = ext;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

}
